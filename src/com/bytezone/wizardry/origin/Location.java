package com.bytezone.wizardry.origin;

import com.bytezone.wizardry.origin.WizardryOrigin.Direction;

// -----------------------------------------------------------------------------------//
public class Location
// -----------------------------------------------------------------------------------//
{
  private int level;
  private int row;
  private int column;

  // ---------------------------------------------------------------------------------//
  public Location (int level, int column, int row)
  // ---------------------------------------------------------------------------------//
  {
    if (column > 19)
      column = 19;
    if (column < 0)
      column = 0;
    if (row > 19)
      row = 19;
    if (row < 0)
      row = 0;

    this.level = level;
    this.row = row;
    this.column = column;
  }

  // ---------------------------------------------------------------------------------//
  public Location (int[] aux)
  // ---------------------------------------------------------------------------------//
  {
    this.level = aux[0];
    this.row = aux[1];
    this.column = aux[2];
  }

  // ---------------------------------------------------------------------------------//
  public boolean matches (Location other)
  // ---------------------------------------------------------------------------------//
  {
    return level == other.level && row == other.row && column == other.column;
  }

  // ---------------------------------------------------------------------------------//
  public int getLevel ()
  // ---------------------------------------------------------------------------------//
  {
    return level;
  }

  // ---------------------------------------------------------------------------------//
  public int getRow ()
  // ---------------------------------------------------------------------------------//
  {
    return row;
  }

  // ---------------------------------------------------------------------------------//
  public int getColumn ()
  // ---------------------------------------------------------------------------------//
  {
    return column;
  }

  // ---------------------------------------------------------------------------------//
  public void set (int row, int column)
  // ---------------------------------------------------------------------------------//
  {
    this.row = row;
    this.column = column;
  }

  // ---------------------------------------------------------------------------------//
  public void move (Direction direction)
  // ---------------------------------------------------------------------------------//
  {
    switch (direction)
    {
      case SOUTH:
        row--;
        if (row < 0)
          row = 19;
        break;

      case NORTH:
        row++;
        if (row > 19)
          row = 0;
        break;

      case EAST:
        column++;
        if (column > 19)
          column = 0;
        break;

      case WEST:
        column--;
        if (column < 0)
          column = 19;
        break;
    }
  }

  // ---------------------------------------------------------------------------------//
  @Override
  public String toString ()
  // ---------------------------------------------------------------------------------//
  {
    return String.format ("L:%02d  N:%02d  E:%02d", level, row, column);
  }
}

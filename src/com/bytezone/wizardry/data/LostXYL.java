package com.bytezone.wizardry.data;

// -----------------------------------------------------------------------------------//
public class LostXYL
// -----------------------------------------------------------------------------------//
{
  int[] value = new int[4];

  // ---------------------------------------------------------------------------------//
  public LostXYL (byte[] buffer, int offset)
  // ---------------------------------------------------------------------------------//
  {
    for (int i = 0; i < 4; i++)
      value[i] = Utility.getShort (buffer, offset + i * 2);
  }

  // ---------------------------------------------------------------------------------//
  @Override
  public String toString ()
  // ---------------------------------------------------------------------------------//
  {
    return String.format ("%04X %04X %04X %04X", value[0], value[1], value[2], value[3]);
  }
}
